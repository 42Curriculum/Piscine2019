int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int n;

	i = 0;
	while (tab[i] != 0)
	{
		n = 0;
		if (f(tab[i]) == 1)
			return (1);
		i++;
	}
	return (0);
}
