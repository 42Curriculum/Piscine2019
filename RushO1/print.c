#include <unistd.h>

void print(char **sudoku)
{
	int i;
	int j;

	i = 1;
	while (i <= 9)
	{
		j = 0;
		while (j < 9)
		{
			if (j != 0)
				write(1, " ", 1);
			write(1, &sudoku[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
