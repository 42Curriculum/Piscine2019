#include <stdio.h>

int	ft_sqrt(int nb)
{
	int d;

	d = 1;
	while (d != nb)
	{
		if (d != (nb / d))
		{
			d++;
		}
		else
		{
			return (d);
		}
	}
	return (0);
}
